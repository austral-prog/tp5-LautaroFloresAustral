def max_of_two(x, y):
    x = int(input('Ingrese el primer número: '))
    y = int(input('Ingrese el segundo número: '))
    mayor = max(x, y)
    return mayor


def max_of_three(x, y, z):
    x = int(input('Ingrese el primer número: '))
    y = int(input('Ingrese el segundo número: '))
    z = int(input('Ingrese el tercer número: '))
    mayor = max(x, y, z)
    return mayor

